// 价格解析工具函数
function parsePrice(priceStr) {
    if (!priceStr) return 0;

    const str = String(priceStr).trim();

    // 处理'w'表示万
    if (str.toLowerCase().includes('w')) {
        const num = parseFloat(str.toLowerCase().replace('w', ''));
        return isNaN(num) ? 0 : num * 10000;
    }

    // 处理'/'分隔的范围，取平均值
    if (str.includes('/')) {
        const parts = str.split('/');
        const p1 = parsePrice(parts[0]);
        const p2 = parsePrice(parts[1]);
        if (p1 > 0 && p2 > 0) return (p1 + p2) / 2;
        if (p1 > 0) return p1;
        if (p2 > 0) return p2;
    }

    // 直接解析数字
    const num = parseFloat(str);
    return isNaN(num) ? 0 : num;
}

// 估价计算函数
function calculatePrice(selections) {
    let total = 0;

    // 角色价格
    if (selections.character) {
        const charRules = PRICE_RULES.character["69"]?.characters || [];
        const charMatch = charRules.find(c =>
            c.type === selections.character.type ||
            c.limit_item === selections.character.limitItem
        );
        if (charMatch && charMatch.base_price) {
            total += charMatch.base_price;
        }
        if (charMatch && charMatch.extra_price) {
            total += charMatch.extra_price;
        }
    }

    // 这里可以添加其他装备类型的计算逻辑

    return Math.round(total);
}

